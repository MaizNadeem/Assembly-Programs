	ORG 100h

	.DATA

		STR1 	DB 	'MAIZ NADEEM'
		SIZE 	DW	11
		KFLAG	DB  0

	.CODE

	MAIN PROC



		MOV BX, 0
		MOV ES, BX
		
		CLI
		MOV ES:[0x9*4], OFFSET [KBISR]
		MOV ES:[0x9*4+2], CS
		STI

		MOV AH, 0
		MOV AL, 3
		INT 10h

		MOV AX, 0xB800
		MOV ES, AX
		MOV SI, (13-5-1)*160 + (40-1)*2

		MOV CX, SIZE
		MOV AH, 0x07
		XOR BX, BX

		NAMEPRINT:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					ADD SI, 80*2
					INC BX
					LOOP NAMEPRINT


		PROCCALL:	CMP KFLAG, 1
					JNE SKIP
					CALL RIGHT
					
			SKIP:	JMP PROCCALL

	
	RET
	MAIN ENDP
	

			KBISR:	PUSH AX
				 	IN  AL, 0x60
					CMP AL, 0x19
					JNE KNEXTCOMP
					MOV KFLAG, 1
					

	    KNEXTCOMP: 	CMP AL, 0x99
	    			JNE KEXIT
	    			MOV KFLAG, 0

			KEXIT:	MOV AL, 0x020
					OUT 0x20, AL
					POP AX
					IRET


	RIGHT PROC

		MOV CX, SIZE

		ERASE:		SUB SI, 80*2
					MOV ES:SI, 0X0720
					LOOP ERASE

					MOV CX, SIZE
					MOV BX, 0
					ADD SI, 2
					CMP SI, 1280
					JGE	SHIFTLEFT

		NAMEPRINT2:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					ADD SI, 80*2
					INC BX
					LOOP NAMEPRINT2
	RET

		SHIFTLEFT:	MOV SI, (13-5-1)*160

		NAMEPRINT3:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					INC BX
					ADD SI, 80*2
					LOOP NAMEPRINT3

	RET
	RIGHT ENDP