	ORG 100h

	.DATA

		STR1 	DB 	'MAIZ NADEEM'
		SIZE 	DW	10

	.CODE

	MAIN PROC

		MOV AX, 0XB800
		MOV ES, AX
		MOV SI, (13-1)*160 + (40-5-1)*2

		MOV CX, SIZE
		MOV AH, 0X07
		XOR BX, BX

		NAMEPRINT:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					ADD SI, 2
					INC BX
					LOOP NAMEPRINT

		PROCCALL:	CALL DOWN
					LOOP PROCCALL
		 
	MAIN ENDP
	RET

	DOWN PROC

		MOV CX, SIZE

		ERASE:		SUB SI, 2
					MOV ES:SI, 0X0720
					LOOP ERASE

					MOV CX, SIZE
					MOV BX, 0
					ADD SI, 160
					CMP SI, 80*25*2
					JG 	SHIFTTOP

		NAMEPRINT2:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					ADD SI, 2
					INC BX
					LOOP NAMEPRINT2
	RET

		SHIFTTOP:	MOV SI, (40-5-1)*2

		NAMEPRINT3:	MOV AL, [STR1 + BX]
					MOV ES:SI, AX
					INC BX
					ADD SI, 2
					LOOP NAMEPRINT3

	DOWN ENDP

	RET
